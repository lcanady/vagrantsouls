export function lookupSideQuest(roll: number): SideQuest | undefined {
  return SIDE_QUESTS.find(q => roll >= q.minRoll && roll <= q.maxRoll);
}

export function getSideQuest(id: string): SideQuest | undefined {
  return SIDE_QUESTS.find(q => q.id === id);
}
