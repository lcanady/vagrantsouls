// YE – YELLOW EVENTS TABLE (Book 8: The Forgotten Tome of Curious Rules)
// 100 events triggered when an adventurer enters a yellow dungeon area.

export type YEEventType =
  | "trap"
  | "hazard"
  | "encounter"
  | "discovery"
  | "special";

export interface YEMonster {
  name: string;
  av: number;
  def: number;
  dmg: number;
  hp: string;
  killReward: string;
  abilities: string[];
  note?: string;
}

/** A d6 sub-table entry (used by rune events, swarm events, etc.) */
export interface YED6Entry {
  roll: number;
  effect: string;
  monster?: YEMonster;
}

/** A d100 sub-table entry (used by spider/golem/guard/prisoner tables) */
export interface YED100Entry {
  minRoll: number;
  maxRoll: number;
  label: string;
  monster?: YEMonster;
  reward?: string;
}

export interface YEEvent {
  roll: number;
  title: string;
  type: YEEventType;
  /** Plain-English summary of the event effect */
  summary: string;
  /** Named skill test (if any) */
  test?: {
    name: string;
    stat: "Str" | "Dex" | "Int";
    modifier: number;
    success: string;
    fail: string;
    skills: string[];
  };
  /** Specific monster(s) encountered in this event */
  monsters?: YEMonster[];
  /** d6 sub-table entries (rune effects, swarm types, etc.) */
  d6Table?: YED6Entry[];
  /** d100 sub-table entries (golem/spider/guard types, prisoner rewards, etc.) */
  d100Table?: YED100Entry[];
  /** Notable items or rewards found during this event */
  rewards?: string[];
  /** Additional mechanical notes */
  note?: string;
}

