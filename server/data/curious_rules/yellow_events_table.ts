/** Look up a Yellow Event by d100 roll (1–100) */
export function getYellowEvent(roll: number): YEEvent {
  const event = YELLOW_EVENTS.find((e) => e.roll === roll);
  if (!event) throw new Error(`YE roll must be 1-100, got ${roll}`);
  return event;
}
